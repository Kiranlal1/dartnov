void main(){
  int Year=15;
  switch(Year){
    case 11:
      print("2011");
      break;
    case 12:
      print("2012");
      break;
    case 13:
      print("2013");
      break;
    case 14:
      print("2014");
      break;
    case 15:
      print("2015");
      break;
    case 16:
      print("2016");
      break;
    case 17:
      print("2017");
      break;
    default:
      print("2023");
  }
}