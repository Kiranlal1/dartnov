void main(){
  String week="thu";
  switch(week){
    case "mon" :
      print("Monday");
      break;
    case "tue" :
      print("Tuesday");
      break;
    case "wed" :
      print("Wednesday");
      break;
    case "thu" :
      print("Thursday");
      break;
    case "fry" :
      print("Friday");
      break;
    default :
      print("Saturday");
  }
}